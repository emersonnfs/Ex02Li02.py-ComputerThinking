camisa = int(input("Digite quantas camisas você tem: "))

calca = int(input("Digite quantas calças você tem: "))

sapato = int(input("Digite quantos pares de sapatos você tem: "))

variedade = camisa * calca * sapato

print('Com', camisa, 'camisas ,', calca, 'calças e', sapato, 'pares de sapatos, você terá', variedade, 'combinações de roupas.')